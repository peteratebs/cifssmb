/*----------------------------------------------------------------------*
                         rtp_get_system_msec
 *----------------------------------------------------------------------*/
unsigned long rtp_get_system_msec (void)
{
unsigned long elapsed_msec;

    elapsed_msec = GetTickCount();
    return (elapsed_msec);
}


/*----------------------------------------------------------------------*
                         rtp_get_system_sec
 *----------------------------------------------------------------------*/
unsigned long rtp_get_system_sec (void)
{
unsigned long elapsed_sec;

    elapsed_sec = (GetTickCount() / 1000);
    return (elapsed_sec);
}


